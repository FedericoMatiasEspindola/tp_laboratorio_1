
int sumaNum (int a, int b)
{
    int resultado;
    resultado = a+b;
    return resultado;
}

int restaNum (int a, int b)
{
    int resultado;
    resultado = a-b;
    return resultado;
}

int divNum (int a, int b)
{
    int resultado;
    resultado = a/b;
    return resultado;
}

int multNum (int a, int b)
{
    int resultado;
    resultado = a*b;
    return resultado;
}
